package amdantas.errado.corrigido;

public class Aluno {
	private String nome;
	private String maticula;
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getMaticula() {
		return maticula;
	}
	public void setMaticula(String maticula) {
		this.maticula = maticula;
	}
}
